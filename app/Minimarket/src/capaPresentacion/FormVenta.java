    private void mostrarDatosTable() {
        Modelo = new DefaultTableModel(datos, Titulo);
        TablaProductos.setModel(Modelo);
        for (int i = 0; i < bdproducto.numeroProductos(); i++) {
            Producto dat = bdproducto.obtenerProducto(i);
            Object[] fila = {dat.getCodigo(), dat.getNombre(), dat.getPrecio(), dat.getStock(), dat.getCategoria()};
            Modelo.addRow(fila);
        }
    }

    private void mostrarItemTable() {
        Modelo2 = new DefaultTableModel(datos2, Titulo2);
        TablaDetalleVenta.setModel(Modelo2);
        for (int i = 0; i < bddetalleventa.numeroDetalleVentas(); i++) {
            DetalleVenta dat = bddetalleventa.obtenerDetalleVenta(i);
            if (dat.getNumVenta() == Integer.parseInt(campo_NumVenta.getText())) {
                String nomProducto = bdproducto.buscarProducto(dat.getCodPro()).getNombre();
                Object[] fila = {dat.getItem(), nomProducto, dat.getPrecio(), dat.getCan(), dat.getSubTot()};
                Modelo2.addRow(fila);
            }
        }
    }

    private void actualizarStock() {
        for (int i = 0; i < bddetalleventa.numeroDetalleVentas(); i++) {
            DetalleVenta dat = bddetalleventa.obtenerDetalleVenta(i);
            if (dat.getNumVenta() == Integer.parseInt(campo_NumVenta.getText())) {
                int sto = bdproducto.buscarProducto(dat.getCodPro()).getStock();
                int can = dat.getCan();
                bdproducto.actualizarStock(dat.getCodPro(), sto - can);
            }
        }
    }

    private void calcular() {
        double total = 0.0;
        for (int i = 0; i < bddetalleventa.numeroDetalleVentas(); i++) {
            DetalleVenta dat = bddetalleventa.obtenerDetalleVenta(i);
              if (dat.getNumVenta() == Integer.parseInt(campo_NumVenta.getText())) 
                total = total + dat.getSubTot();
        }
        txtTotal.setText("" + total);
    }

