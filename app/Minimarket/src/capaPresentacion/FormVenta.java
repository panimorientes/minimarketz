    private void campo_XcantidadKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campo_XcantidadKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int cant = Integer.parseInt(campo_Xcantidad.getText());
            double preU = Double.parseDouble(campo_Xpreciounit.getText());
            if (cant > 0) {
                campo_Xtotal.setBackground(Color.white);
                campo_Xtotal.setText(String.valueOf(cant * preU));
            } else {
                JOptionPane.showMessageDialog(null, "Cantidad INCORRECTA!");
                campo_Xcantidad.setText(null);
            }
        }
    }//GEN-LAST:event_campo_XcantidadKeyPressed

